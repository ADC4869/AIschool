<script>
// HIỆN CÁC LỚP
function showSection(khoi) {
    // Ẩn tất cả các lớp
    const sections = document.querySelectorAll('.main__class');
    sections.forEach(section => {
        section.classList.add('hidden');
    });

    // Hiện lớp tương ứng với khối được chọn
    const selectedSection = document.getElementById(khoi);
    if (selectedSection) {
        selectedSection.classList.remove('hidden');
    }
}

// Mở modal
document.querySelectorAll('.btn__thongtinlop').forEach(button => {
    button.addEventListener('click', function() {
        const modalId = this.id.replace('openModal-', 'infoModal-');
        document.getElementById(modalId).classList.remove('hidden');
    });
});

// Đóng modal khi nhấn nút đóng
document.querySelectorAll('.close-modal').forEach(button => {
    button.addEventListener('click', function() {
        this.closest('.modal').classList.add('hidden');
    });
});

// Đóng modal khi nhấn ra ngoài modal
window.addEventListener('click', function(event) {
    if (event.target.classList.contains('modal')) {
        event.target.classList.add('hidden');
    }
});
</script>