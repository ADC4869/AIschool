<script>
    // Lấy các phần tử "siso", "tre", "vang", 'hiendien'
    const sisoBtn = document.getElementById("siso");
    const hiendienBtn = document.getElementById("hiendien");
    const treBtn = document.getElementById("tre");
    const vangBtn = document.getElementById("vang");

    // Lấy danh sách học sinh
    const allStudents = document.querySelectorAll(".student-item");

    // Hàm hiển thị tất cả học sinh
    function showAllStudents() {
        allStudents.forEach(student => {
            student.style.display = "flex";
        });
    }


    // Hàm hiển thị hiendien
    function showhiendienStudents() {
        allStudents.forEach(student => {
            if (student.classList.contains("hiendien")) {
                student.style.display = "flex";
            } else {
                student.style.display = "none";
            }
        });
    }

    // Hàm hiển thị tre
    function showtreStudents() {
        allStudents.forEach(student => {
            if (student.classList.contains("tre")) {
                student.style.display = "flex";
            } else {
                student.style.display = "none";
            }
        });
    }
    // vắng
    function showvangStudents() {
        allStudents.forEach(student => {
            if (student.classList.contains("vang")) {
                student.style.display = "flex";
            } else {
                student.style.display = "none";
            }
        });
    }

    // Gán sự kiện click cho các phần tử
    sisoBtn.addEventListener("click", showAllStudents);
    hiendienBtn.addEventListener("click", showhiendienStudents);
    treBtn.addEventListener("click", showtreStudents);
    vangBtn.addEventListener("click", showvangStudents);

    document.getElementById('siso').addEventListener('click', function () {
        document.getElementById('siso').classList.add('active');
        document.getElementById('hiendien').classList.remove('active');
        document.getElementById('tre').classList.remove('active');
        document.getElementById('vang').classList.remove('active');
    });

    document.getElementById('hiendien').addEventListener('click', function () {
        document.getElementById('siso').classList.remove('active');
        document.getElementById('hiendien').classList.add('active');
        document.getElementById('tre').classList.remove('active');
        document.getElementById('vang').classList.remove('active');
    });
    document.getElementById('tre').addEventListener('click', function () {
        document.getElementById('siso').classList.remove('active');
        document.getElementById('hiendien').classList.remove('active');
        document.getElementById('tre').classList.add('active');
        document.getElementById('vang').classList.remove('active');
    });

    document.getElementById('vang').addEventListener('click', function () {
        document.getElementById('siso').classList.remove('active');
        document.getElementById('hiendien').classList.remove('active');
        document.getElementById('tre').classList.remove('active');
        document.getElementById('vang').classList.add('active');
    });
</script>